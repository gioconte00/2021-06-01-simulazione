package it.polito.tdp.genes.model;

public class GeniConIng {
	
	private Genes gene;
	private int numINg;
	
	
	public GeniConIng(Genes gene, int numINg) {
		super();
		this.gene = gene;
		this.numINg = numINg;
	}
	
	
	public Genes getGene() {
		return gene;
	}
	public int getNumINg() {
		return numINg;
	}
	
	

}
